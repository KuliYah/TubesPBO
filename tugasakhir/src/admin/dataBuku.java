    private void btnCariBukuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariBukuActionPerformed
        int row = jTable1.getRowCount();
        for(int a=0; a<row; a++) {
            modell.removeRow(0);
        }
        try {
            Connection konek = DriverManager.getConnection("jdbc:mysql://localhost:3306/tugasakhir","root","");
            ResultSet rs = konek.createStatement().executeQuery("SELECT * FROM buku where judul_buku='"+inputCariBuku.getText()+"'");
            while (rs.next()) {
                String data[] = {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4)};
                modell.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(peminjamanBuku.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnCariBukuActionPerformed

