    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int i = jTable1.getSelectedRow();
        if(i>-1) {
            inputIdBuku.setText(modell.getValueAt(i, 0).toString());
            inputIdBuku.setEnabled(false);
            
            inputJudulBuku.setText(modell.getValueAt(i, 1).toString());
            
            inputPenerbit.setText(modell.getValueAt(i, 2).toString());
            inputStok.setText(modell.getValueAt(i, 3).toString());
            
            btnSubmitBuku.setEnabled(false);
            btnBackBuku.setEnabled(true);
        }
    }//GEN-LAST:event_jTable1MouseClicked

